interface WeightedEdgeFunctions
{
    /**
     * Returns a string representation of the Weighted Edge.
     * 
     * Form is "(x,y,w)", where:
     * x - is the from vertex
     * y - is the to vertex
     * w - is the weight
     * 
     * @return string representation of this Weighted Edge.
     */ 
    public String toString();
}
